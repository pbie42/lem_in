#include "lem_in.h"

int		list_length(t_qv *list)
{
	int	i;
	t_qv	*tmp;
	
	tmp = list;
	i = 0;
	if (!list)
		return (0);
	while (tmp->next)
	{
		tmp = tmp->next;
		i++;
	}
	i++;
	return (i);
}
