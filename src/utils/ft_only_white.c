#include "lem-in.h"

t_bool	ft_only_white(char *s)
{
	int	i;

	i = -1;
	while(s[++i]){
		if (s[i] == ' ' || s[i] == '\t' || s[i] == '\r' || s[i] == '\v'
		|| s[i] == '\f' || s[i] == '\n')
			;
		else
			return (FALSE);
	}
	return (TRUE);
}
